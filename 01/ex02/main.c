#include <unistd.h>

void	ft_swap(int *a, int *b);

int main(void)
{
	int a;
	int b;
	char A;
	char B;

	a = 9;
	b = 4;
	A = '0' + a;
	B = '0' + b;
	write(1, &A, 1);
	write(1, &B, 1);

	ft_swap(&a, &b);

	A = '0' + a;
	B = '0' + b;
	write(1, &A, 1);
	write(1, &B, 1);
}
	
